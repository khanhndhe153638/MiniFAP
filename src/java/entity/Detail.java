package entity;

/**
 *
 * @author ADMIN
 */
public class Detail {
    private int userID;
    private String user;
    private String fullname;
    private String role;
    private String email;

    public Detail() {
    }

    public Detail(int userID, String user, String fullname, String role, String email) {
        this.userID = userID;
        this.user = user;
        this.fullname = fullname;
        this.role = role;
        this.email = email;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
}
